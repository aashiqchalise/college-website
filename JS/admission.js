window.onload = function () {
    var provienceSel = document.getElementById("provienceSel"),
    districtSel = document.getElementById("districtSel"),
    municipalitySel = document.getElementById("municipalitySel"),
    wardnoSel = document.getElementById("wardnoSel");
    for (var provience in addressObject) {
        provienceSel.options[provienceSel.options.length] = new Option(provience, provience);
    }
    provienceSel.onchange = function () {
        districtSel.length = 1;       // remove all options bar first
        municipalitySel.length = 1;   // remove all options bar first
        wardnoSel.length = 1;         // remove all options bar first
        if (this.selectedIndex < 1) return; // done 
        for (var district in addressObject[this.value]) {
            districtSel.options[districtSel.options.length] = new Option(district, district);
        }
    }
    provienceSel.onchange(); // reset in case page is reloaded

    districtSel.onchange = function () {
        municipalitySel.length = 1;   // remove all options bar first
        wardnoSel.length = 1;         // remove all options bar first
        if (this.selectedIndex < 1) return; // done 
        for (var municipality in addressObject[provienceSel.value][this.value]) {
            municipalitySel.options[municipalitySel.options.length] = new Option(municipality, municipality);
        }
    }

    municipalitySel.onchange = function () {
        wardnoSel.length = 1; // remove all options bar first
        if (this.selectedIndex < 1) return; // done 
        var wardno = addressObject[provienceSel.value][districtSel.value][this.value];
        for (var i = 0; i < wardno.length; i++) {
            wardnoSel.options[wardnoSel.options.length] = new Option(wardno[i], wardno[i]);
        }
    }
}
